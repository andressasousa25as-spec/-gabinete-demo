// Helpers de consulta
export function getEleitos(municipio) {
  const dados = municipio === 'macapa' ? VEREADORES_2024.macapa : VEREADORES_2024.santana
  return dados.candidatos.filter(c => c.eleito)
}

export function getCandidatosPorPartido(municipio, partido) {
  const dados = municipio === 'macapa' ? VEREADORES_2024.macapa : VEREADORES_2024.santana
  return dados.candidatos.filter(c => c.partido === partido)
}

export function getTopVotados(municipio, n = 10) {
  const dados = municipio === 'macapa' ? VEREADORES_2024.macapa : VEREADORES_2024.santana
  return dados.candidatos.slice(0, n)
}

export function calcularQuocienteEleitoral(municipio) {
  const dados = municipio === 'macapa' ? VEREADORES_2024.macapa : VEREADORES_2024.santana
  return Math.floor(dados.totalVotosValidos / dados.totalEleitos)
}
